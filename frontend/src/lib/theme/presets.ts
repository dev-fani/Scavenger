export const allPresets: Theme[] = [
  defaultLightTheme,
  defaultDarkTheme,
  oceanTheme,
  forestTheme,
  sunsetTheme,
  midnightTheme,
  highContrastTheme,
];

export const getThemeById = (id: string): Theme | undefined => {
  return allPresets.find(theme => theme.id === id);
};

export const getThemesByMode = (mode: 'light' | 'dark'): Theme[] => {
  return allPresets.filter(theme => theme.mode === mode);
};

export const getThemesByTag = (tag: string): Theme[] => {
  return allPresets.filter(theme => theme.tags?.includes(tag));
};
